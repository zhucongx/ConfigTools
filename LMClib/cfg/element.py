    def __str__(self, name: str = '', ) -> str:
        return (f"{self.symbol}, Atomic number: {self.atomic_number},"
                f" Atomic weight: {self.mass}")

    @classmethod
    def from_atomic_number(cls, atomic_number: int):
        for element in cls:
            if element.atomic_number == atomic_number:
                return element
        raise ValueError(f"No element found with atomic number {atomic_number}")

    @classmethod
    def from_symbol(cls, symbol: str):
        for element in cls:
            if element.symbol == symbol:
                return element
        raise ValueError(f"No element found with symbol {symbol}")


